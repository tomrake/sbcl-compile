#!/usr/bash
echo "BOOTING_COMPILER=${BOOTING_COMPILER}"
echo "BOOTING_COMPILER_OPTIONS=${BOOTING_COMPILER_OPTIONS}"
BOOT_CONFIG="--xc-host=${BOOTING_COMPILER} ${BOOTING_COMPILER_OPTIONS}"
echo "BOOT_CONFIG=${BOOT_CONFIG}"
cd ${RELEASE_VERSION}
mkdir "${BUILD_LOCATION}"
tar -xf sbcl-${RELEASE_VERSION}-source.tar.bz2 -C "${BUILD_LOCATION}"
cd "${BUILD_LOCATION}/sbcl-${RELEASE_VERSION}"
echo "sh make.sh ${BOOT_CONFIG}" ${COMPILE_BUILD_OPTIONS} 
sh make.sh "${BOOT_CONFIG}" ${COMPILE_BUILD_OPTIONS} | tee "${BUILD_LOG}"


